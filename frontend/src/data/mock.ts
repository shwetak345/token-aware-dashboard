export const chartData: TokenDataPoint[] = commits
  .slice()
  .reverse()
  .map((c) => ({
    commit: c.sha,
    tokens: c.tokens,
    rolling: 0,
    isAnomaly: c.isAnomaly,
  }))
  .map((point, idx, arr) => {
    const window = arr.slice(Math.max(0, idx - 2), idx + 1);
    return {
      ...point,
      rolling: Math.round(window.reduce((s, p) => s + p.tokens, 0) / window.length),
    };
  });

